#.data
#    .word 0,6,0,16,0x80000000,0x80000100,0x100,5,0   #编译成机器码时，编译器会在前面多加个0，所以后面lw指令地址会多加4

_start:    
		addi x8,x0,3       	#t0=3   0
        addi x9,x8,2   		#t1=5	4
		addi x10,x0,1       	#t2=1	8
		addi x11,x0,0			#t3=0	12

        add  x16,x9,x8  		#s0=t1+t0=8  测试add指令	16
        add  x16,x16,x16		#s0=s0+s0=16				20
		lw   x17, 12(x0)  		#							24
        beq  x17,x16,_next1		#正确跳到_next 				28
		
		j _fail					

_next1:	
		lw x8, 16(x0)			#t0 = 0x80000000	36
		lw x9, 20(x0)			#t1 = 0x80000100	40
		
		add  x16,x9,x8		#s0 = 0x00000100 = 256	44
		lw x17, 24(x0)			#						48
        beq  x17,x16,_next2		#正确跳到_success		52
		
		j _fail

_next2:
		add x0, x0, x10			#$0应该一直为0			60
		beq x0,x11,_success		#						64
		
		
_fail:  
		sw   x11, 8(x0) #失败通过看存储器地址0x08里值，若为0则测试不通过，最初地址0x08里值为0   64
        j    _fail				#																68

_success: 
		sw   x10, 8(x0)    #全部测试通过，存储器地址0x08里值为1												72
		j   _success       		#																		76

					  #判断测试通过的条件是最后存储器地址0x08里值为1，说明全部通过测试